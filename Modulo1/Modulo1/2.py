# Variables con diferentes tipos de datos

# Tipo entero (int)
edad = 17

# Tipo decimal (float)
altura = 1.75

# Tipo cadena de texto (str)
nombre = "Nicolás"

# Tipo booleano (bool)
es_estudiante = True

# Imprimir cada variable con su tipo de dato
print("Nombre:", nombre)
print("Edad:", edad)
print("Altura:", altura)
print("¿Es estudiante?:", es_estudiante)

# Mostrar el tipo de dato de cada variable
print("Tipo de dato de 'nombre':", type(nombre))
print("Tipo de dato de 'edad':", type(edad))
print("Tipo de dato de 'altura':", type(altura))
print("Tipo de dato de 'es_estudiante':", type(es_estudiante))
