# Operadores Aritméticos
a = 10
b = 3

print("Suma:", a + b)           # 13
print("Resta:", a - b)          # 7
print("Multiplicación:", a * b) # 30
print("División:", a / b)       # 3.333...
print("División entera:", a // b) # 3
print("Módulo:", a % b)         # 1
print("Potencia:", a ** b)      # 1000

# Operadores de Comparación
print("¿a es igual a b?", a == b)    # False
print("¿a es diferente de b?", a != b) # True
print("¿a es mayor que b?", a > b)    # True
print("¿a es menor o igual a b?", a <= b) # False

# Operadores Lógicos
x = True
y = False

print("AND:", x and y)    # False
print("OR:", x or y)      # True
print("NOT x:", not x)    # False
